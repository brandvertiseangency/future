function normalizePlatform(platform: string): SocialPlatform {
  const lower = platform.toLowerCase().trim()
  if (lower === 'x' || lower === 'twitter/x' || lower === 'twitter') return 'twitter'
  if (lower in PLATFORM_META) return lower as SocialPlatform
  return 'instagram'
}

export function SocialIcon({ platform, size = 16, className, branded = false }: SocialIconProps) {
  const key = normalizePlatform(platform)
  const meta = PLATFORM_META[key] ?? PLATFORM_META.instagram

  if (branded) {
    return (
      <span
        className={cn('inline-flex items-center justify-center rounded-lg shrink-0', className)}
        style={{
          width: size + 12,
          height: size + 12,
          background: meta.bg,
        }}
        title={meta.label}
      >
        <svg
          width={size}
          height={size}
          viewBox="0 0 24 24"
          fill="white"
          aria-label={meta.label}
          role="img"
        >
          <path d={meta.path} />
        </svg>
      </span>
    )
  }

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="currentColor"
      className={cn('shrink-0', className)}
      aria-label={meta.label}
      role="img"
      style={{ color: meta.color }}
    >
      <path d={meta.path} />
    </svg>
  )
}

/** Display name for a platform */
export function getPlatformLabel(platform: string): string {
  const key = normalizePlatform(platform)
  return PLATFORM_META[key]?.label ?? platform
}

/** Hex color for a platform */
export function getPlatformColor(platform: string): string {
  const key = normalizePlatform(platform)
  return PLATFORM_META[key]?.color ?? '#888888'
}
